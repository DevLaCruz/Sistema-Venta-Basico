    private void venderBtnTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_venderBtnTxtMouseClicked
        // TODO add your handling code here:
         try {
            int codigoventa = Integer.parseInt(txtcodventa.getText());double total = Double.parseDouble(txtpreciot.getText());
            Date fecha = new Date();
            int idp = Integer.parseInt(txtcodpro.getText());int idc = Integer.parseInt(txtcodcliente.getText());
            int cantidad = Integer.parseInt(txtcantidad.getText());
            double preciou = Double.parseDouble(txtpreciou.getText());
            Producto producto = ProductoLogic.obtenerProducto(idp);
            List<DetalleVenta> ldetalleventa = new ArrayList<>();
            DetalleVenta detalleventa = new DetalleVenta(cantidad, preciou, producto);
            
            ldetalleventa.add(detalleventa);
            Cliente cliente = ClienteLogic.obtenerCliente(idc);
            venta = new Venta(codigoventa, fecha, total, cliente, ldetalleventa);
            

            if(VentaLogic.insertarVenta(venta)){
                JOptionPane.showMessageDialog(this, "Se inserto correctamente");
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo insertar");
            }
            
       } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
        txtcodventa.setText("");
        txtcodcliente.setText("");
        txtcodpro.setText("");
        txtcantidad.setText("");
        txtdni.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txtproducto.setText("");
        txtcategoria.setText("");
        txtpreciou.setText("");
        txtpreciot.setText("");
    }//GEN-LAST:event_venderBtnTxtMouseClicked

