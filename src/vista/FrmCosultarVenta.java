    private void btnconsultartxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnconsultartxtMouseClicked
        // TODO add your handling code here:
         try{
            Venta venta = (Venta) cmbventa.getSelectedItem();
            txtcliente.setText(venta.getCliente().getNombre()+" "+venta.getCliente().getApellidos());
            txtdni.setText(venta.getCliente().getDni());
            txtventaid.setText(String.valueOf(venta.getIdventa()));
            txtpreciot.setText(String.valueOf(venta.getTotal()));
            txtfecha.setText(String.valueOf(venta.getFecha()));
            for (int i = 0; i < venta.getDetalleventa().size(); i++) {
                DetalleVenta item = venta.getDetalleventa().get(i);
                txtproducto.setText(item.getProducto().getNombre());
                txtcategoria.setText(item.getProducto().getCategoria().getNombre());
                txtcantidad.setText(String.valueOf(item.getCantidad()));
                txtpreciou.setText(String.valueOf(item.getProducto().getPrecio()));
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnconsultartxtMouseClicked

