    private void uploadBtnTxt1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_uploadBtnTxt1MouseClicked
        // TODO add your handling code here:
        String fuente="";
        JFileChooser chooser=new JFileChooser();
        FileNameExtensionFilter format = new FileNameExtensionFilter("JPG,PNG","jpg","png");
        chooser.setFileFilter(format);
    
        int rpta= chooser.showOpenDialog(this);
        if (rpta==JFileChooser.APPROVE_OPTION) {
            fuente=chooser.getSelectedFile().getPath();
            Image img=new ImageIcon(fuente).getImage();
            ImageIcon icono=new ImageIcon(img.getScaledInstance(lblimage.getWidth(), lblimage.getHeight(),Image.SCALE_SMOOTH));
            lblimage.setIcon(icono);
       }
    }//GEN-LAST:event_uploadBtnTxt1MouseClicked

