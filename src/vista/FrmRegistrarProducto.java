    private void insertBtnTxtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_insertBtnTxtMouseClicked
        // TODO add your handling code here:
       try{
            int codigo = Integer.parseInt(txtcodigo.getText());
            String nombre = txtnombre.getText();
            double precio = Double.parseDouble(txtprecio.getText());
            int stock = Integer.parseInt(txtstock.getText());
            Categoria cat = (Categoria) cmbcategoria.getSelectedItem();
            Icon img = lblimage.getIcon();
            
            producto = new Producto(codigo,nombre,precio,stock,cat,img);
            if(ProductoLogic.insertarProducto(producto)){
                JOptionPane.showMessageDialog(this, "Se insertó correctamente");
            }else{
                JOptionPane.showMessageDialog(this, "Error al insertar");
            }
            this.cargarTablaProducto();
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex);
        }
        
        txtcodigo.setText("");
        txtnombre.setText("");
        txtprecio.setText("");
        txtstock.setText("");
        lblimage.setIcon(null);
    }//GEN-LAST:event_insertBtnTxtMouseClicked

